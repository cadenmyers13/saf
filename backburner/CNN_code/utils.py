def bleach_element(stru: Structure):
    """Change the elements in the structure so that they only contain letters."""
    stru.element = [only_letters(elem) for elem in stru.element]
    stru.element = H(stru)

def only_letters(s: str):
    """Strip the string so that it only contains the letters."""
    return ''.join([c for c in s if c.isalpha()])

def H(s: str):
    """Strip the string so that it only contains the letters."""
    elements = s.element
    for i in range(len(elements)):
        if elements[i] == 'D':
            elements[i] = 'H'
    return elements

